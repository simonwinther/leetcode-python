from typing import List


def construct2DArray(original: List[int], m: int, n: int) -> List[List[int]]:
    res = []
    if len(original) != m * n:
        return res

    pointer = 0

    for i in range(m):
        res.append(original[pointer: pointer + n])
        pointer += n

    return res


def construct2DArrayFast(original: List[int], m: int, n: int) -> List[List[int]]:
    res = []
    if len(original) != m * n:
        return res

    counter = 0
    for i in range(m):
        temp = []
        for j in range(n):
            temp.append(original[counter])
            counter += 1
        res.append(temp)

    return res


